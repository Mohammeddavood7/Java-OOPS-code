package jan_10_TestExam;

public class Vehicle
{
    String model;
    String make;
    double price;

    public Vehicle(String model,String make, double price) 
    {
        this.model = model;
        this.make = make;
        this.price = price;
    }
    
	public String getModel() {
		return model;
	}

	public String getMake() {
		return make;
	}

	public double getPrice() {
		return price;
	}



	
     
    
    
}

